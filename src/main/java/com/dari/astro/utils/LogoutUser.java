package com.dari.astro.utils;

public class LogoutUser {
	
	private String userEmailId ;
	private String userPhoneNumber;
	private String logoutDeviceOS;
	private String logoutDeviceID;
	private String logoutLocation;
	private String logoutDeviceOSVersion;
	private String logoutDeviceModel;
	
	public String getUserEmailId() {
		return userEmailId;
	}
	public void setUserEmailId(String userEmailId) {
		this.userEmailId = userEmailId;
	}
	public String getUserPhoneNumber() {
		return userPhoneNumber;
	}
	public void setUserPhoneNumber(String userPhoneNumber) {
		this.userPhoneNumber = userPhoneNumber;
	}
	public String getLogoutDeviceOS() {
		return logoutDeviceOS;
	}
	public void setLogoutDeviceOS(String logoutDeviceOS) {
		this.logoutDeviceOS = logoutDeviceOS;
	}
	public String getLogoutDeviceID() {
		return logoutDeviceID;
	}
	public void setLogoutDeviceID(String logoutDeviceID) {
		this.logoutDeviceID = logoutDeviceID;
	}
	public String getLogoutLocation() {
		return logoutLocation;
	}
	public void setLogoutLocation(String logoutLocation) {
		this.logoutLocation = logoutLocation;
	}
	public String getLogoutDeviceOSVersion() {
		return logoutDeviceOSVersion;
	}
	public void setLogoutDeviceOSVersion(String logoutDeviceOSVersion) {
		this.logoutDeviceOSVersion = logoutDeviceOSVersion;
	}
	public String getLogoutDeviceModel() {
		return logoutDeviceModel;
	}
	public void setLogoutDeviceModel(String logoutDeviceModel) {
		this.logoutDeviceModel = logoutDeviceModel;
	}
	

}
